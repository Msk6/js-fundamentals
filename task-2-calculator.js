let num1 = 20
let num2 = 2
let operation = '%'

if (operation == '*') {
    console.log(`${num1} * ${num2} = ${num1 * num2}`)
}else if (operation == '+'){
    console.log(`${num1} + ${num2} = ${num1 + num2}`)
}else if (operation == '-') {
    console.log(`${num1} - ${num2} = ${num1 - num2}`)
} else if (operation == '/') {
    console.log(`${num1} / ${num2} = ${num1 / num2}`)
}else if (operation == '%') {
    console.log(`${num1} % ${num2} = ${num1 % num2}`)
}